    private boolean rellenarCodigo(JTextField j, String cadena)
    {
        if(!j.getText().matches("[a-zA-Z0-9]{1,6}"))
        {
            JOptionPane.showMessageDialog(null, "Código de la "+cadena+" no válido. Debe tener menos de 7  y más de 0 dígitos, y únicamente estar formado por letras y números .", null, WIDTH);
            return false;
        }
        else
        {
            String dni=j.getText();
            while(dni.length()!=6)
            {
                dni="0"+dni;
            }
            j.setText(dni);
            return true;
        }
    }
