    private void jButtonImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonImprimirActionPerformed
        // TODO add your handling code here:
        Informe i = new Informe();
        if(rellenarCodigo(jTextFieldEC1,"izquierda"))
        {
            if(rellenarCodigo(jTextFieldEC2,"derecha")){
                if(jTextFieldEC1.getText().compareTo(jTextFieldEC2.getText())<0){
                    i.ejecutarInforme(jTextFieldEC1.getText(),jTextFieldEC2.getText());
                }else{
                    String cad =jTextFieldEC1.getText();
                    jTextFieldEC1.setText(jTextFieldEC2.getText());
                    jTextFieldEC2.setText(cad);
                    i.ejecutarInforme(jTextFieldEC1.getText(),jTextFieldEC2.getText()); 
                }
            }
            else
            {
                jTextFieldEC2.setText("");
                jTextFieldEC2.grabFocus();
            }
        }
        else
        {
            jTextFieldEC1.setText("");
            jTextFieldEC1.grabFocus();
        }
    }//GEN-LAST:event_jButtonImprimirActionPerformed

