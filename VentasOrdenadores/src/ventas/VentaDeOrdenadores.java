    private void jTextFieldNombreKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldNombreKeyPressed
         //Al pulsar ENTER en la posiciÃ³n de la caja de texto del nombre.
        if(evt.getKeyCode()==KeyEvent.VK_ENTER){
            if(jTextFieldNombre.getText().equals("")){
                JOptionPane.showMessageDialog(this, "La caja de texto del nomnbre no puede estar vacÃ­a.", "ERROR.", JOptionPane.ERROR_MESSAGE);
            }
            else{
                if(jTextFieldNombre.getText().length()>15){
                    JOptionPane.showMessageDialog(this, "La caja de texto no admite mÃ¡s de 15 carÃ¡cteres.", "ERROR.", JOptionPane.ERROR_MESSAGE);
                }else{
                    if(!validarNombre(jTextFieldNombre.getText())){
                        JOptionPane.showMessageDialog(this, "La caja de texto no admite carÃ¡cteres especiales(nÃºmeros, sÃ­mbolos, barras, etc).", "ERROR.", JOptionPane.ERROR_MESSAGE);
                        estadoInicial();
                    }else{
                        estado2();
                    }
                }
            }
        }
    }//GEN-LAST:event_jTextFieldNombreKeyPressed

